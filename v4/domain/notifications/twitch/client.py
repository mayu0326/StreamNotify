
import logging
import httpx
from datetime import datetime, timezone
from typing import Optional, Dict, Any, Literal, Union

from v4.core.config import settings
from v4.core.database import SessionLocal, get_twitch_account

logger = logging.getLogger("v4.twitch.client")

class TwitchClient:
    """
    Twitch API Client wrapping Center Server API.
    Interacts with Center Server to manage Twitch subscription and fetch user data.
    """
    def __init__(self):
        self.base_url = settings.center_server_url.rstrip("/")
        self.api_key = settings.websub_client_api_key
        self.callback_base_url = settings.websub_callback_base_url.rstrip("/")

    def _get_headers(self) -> Dict[str, str]:
        return {
            "X-Client-Id": settings.websub_client_id,
            "X-Client-API-Key": self.api_key,
            "Content-Type": "application/json"
        }

